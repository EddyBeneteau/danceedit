const rates = { EUR: 1, USD: 1.08, GBP: 0.86 };
const symbols = { EUR: '€', USD: '$', GBP: '£' };

let lang = 'fr';
let currency = 'EUR';
let testimonialIndex = 0;
let testimonialTimer;

function detectDefaults() {
  const browserLang = (navigator.language || 'fr').toLowerCase();
  if (browserLang.startsWith('fr')) lang = 'fr';
  else if (browserLang.startsWith('es')) lang = 'es';
  else lang = 'en';

  const locale = (Intl.NumberFormat().resolvedOptions().locale || 'fr-FR').toLowerCase();
  if (locale.includes('gb')) currency = 'GBP';
  else if (locale.includes('us')) currency = 'USD';
  else if (['fr','es','de','it','pt','nl','be'].some(v => locale.includes(v))) currency = 'EUR';
  else currency = 'USD';
}

function formatPrice(value) {
  if (value == null) return '';
  return `${Math.round(value * rates[currency])} ${symbols[currency]}`;
}

function updateSeo() {
  const t = data[lang];
  document.documentElement.lang = lang;
  document.title = t.pageTitle;
  const description = document.querySelector('meta[name="description"]');
  if (description) description.setAttribute('content', t.pageDescription);
  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle) ogTitle.setAttribute('content', t.pageTitle);
  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc) ogDesc.setAttribute('content', t.pageDescription);
}

function updateTextNodes() {
  const t = data[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });
}

function renderFeatures() {
  const grid = document.getElementById('feature-grid');
  grid.innerHTML = '';
  data[lang].features.forEach(([title, desc]) => {
    const item = document.createElement('div');
    item.className = 'card';
    item.innerHTML = `<div class="card-title">${title}</div><div class="card-desc">${desc}</div>`;
    grid.appendChild(item);
  });
}

function renderPricing() {
  const grid = document.getElementById('pricing-grid');
  grid.innerHTML = '';
  data[lang].pricing.forEach(plan => {
    const card = document.createElement('div');
    card.className = 'card';
    card.innerHTML = `
      <div class="price-name">${plan.name}</div>
      <div class="price-value">${formatPrice(plan.price)}</div>
      ${plan.sample ? `<a href="#" class="sample-link">${plan.sample}</a>` : ''}
      <ul class="price-list">${plan.items.map(item => `<li>${item}</li>`).join('')}</ul>
    `;
    grid.appendChild(card);
  });
}

function renderStyles() {
  const grid = document.getElementById('styles-grid');
  grid.innerHTML = '';
  data[lang].styles.forEach(item => {
    const el = document.createElement('div');
    el.className = 'mini-card';
    el.textContent = item;
    grid.appendChild(el);
  });
}

function renderSteps() {
  const grid = document.getElementById('steps-grid');
  grid.innerHTML = '';
  data[lang].steps.forEach(item => {
    const el = document.createElement('div');
    el.className = 'step-card';
    el.textContent = item;
    grid.appendChild(el);
  });
}

function renderTestimonial() {
  const set = data[lang].testimonials;
  const [text, author] = set[testimonialIndex % set.length];
  document.getElementById('testimonial-text').textContent = text;
  document.getElementById('testimonial-author').textContent = author;
  const dotsWrap = document.getElementById('testimonial-dots');
  dotsWrap.innerHTML = '';
  set.forEach((_, i) => {
    const dot = document.createElement('div');
    dot.className = `dot${i === testimonialIndex % set.length ? ' active' : ''}`;
    dotsWrap.appendChild(dot);
  });
}

function setWhatsappLink() {
  const encoded = encodeURIComponent(data[lang].whatsappText);
  document.getElementById('whatsapp-float').href = `https://wa.me/33650858622?text=${encoded}`;
}

function updateActiveButtons() {
  document.querySelectorAll('[data-lang]').forEach(btn => btn.classList.toggle('active', btn.dataset.lang === lang));
  document.querySelectorAll('[data-currency]').forEach(btn => btn.classList.toggle('active', btn.dataset.currency === currency));
}

function renderAll() {
  updateSeo();
  updateTextNodes();
  renderFeatures();
  renderPricing();
  renderStyles();
  renderSteps();
  renderTestimonial();
  setWhatsappLink();
  updateActiveButtons();
}

function startSlider() {
  clearInterval(testimonialTimer);
  testimonialTimer = setInterval(() => {
    testimonialIndex = (testimonialIndex + 1) % data[lang].testimonials.length;
    renderTestimonial();
  }, 4000);
}

document.querySelectorAll('[data-lang]').forEach(btn => {
  btn.addEventListener('click', () => {
    lang = btn.dataset.lang;
    testimonialIndex = 0;
    renderAll();
    startSlider();
  });
});

document.querySelectorAll('[data-currency]').forEach(btn => {
  btn.addEventListener('click', () => {
    currency = btn.dataset.currency;
    renderPricing();
    updateActiveButtons();
  });
});

detectDefaults();
renderAll();
startSlider();
